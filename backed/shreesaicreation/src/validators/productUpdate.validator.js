import Joi from 'joi';
import {
  PRODUCT_TYPES,
  PRODUCT_STYLES,
  MOUNT_TYPES,
  ROOM_TYPES,
  BULB_TYPES,
  COLOR_TEMPERATURES,
  INSTALLATION_TYPES,
} from '../constants/lighting.constants.js';

export const productUpdateSchema = Joi.object({
  name: Joi.string().trim().min(2).max(200),
  slug: Joi.string().trim().allow('', null),
  hsnCode: Joi.string().allow(''),
  brand: Joi.string(),
  categoryIds: Joi.array().items(Joi.string().hex().length(24)),
  collectionIds: Joi.array().items(Joi.string().hex().length(24)),
  status: Joi.string().valid('draft', 'published', 'archived'),
  visibility: Joi.string().valid('public', 'hidden'),
  shortDescription: Joi.string().allow(''),
  description: Joi.string().allow(''),
  highlights: Joi.array().items(Joi.string()),
  tags: Joi.array().items(Joi.string()),
  taxPercent: Joi.number().min(0).max(100),
  isTaxInclusive: Joi.boolean(),
  optionDefinitions: Joi.array().items(
    Joi.object({
      name: Joi.string().required(),
      values: Joi.array().items(Joi.string()).default([]),
    })
  ),
  productType: Joi.string().valid(...PRODUCT_TYPES),
  style: Joi.array().items(Joi.string().valid(...PRODUCT_STYLES)),
  mountType: Joi.string().valid(...MOUNT_TYPES),
  roomTypes: Joi.array().items(Joi.string().valid(...ROOM_TYPES)),
  materials: Joi.array().items(Joi.string()),
  finish: Joi.array().items(Joi.string()),
  primaryColor: Joi.string().allow(''),
  bulbType: Joi.string().valid(...BULB_TYPES),
  bulbIncluded: Joi.boolean(),
  numberOfLights: Joi.number().integer().min(0).allow(null),
  maxWattage: Joi.number().allow(null),
  totalWattage: Joi.number().allow(null),
  voltage: Joi.string(),
  colorTemperature: Joi.array().items(Joi.string().valid(...COLOR_TEMPERATURES)),
  lumens: Joi.number().allow(null),
  cri: Joi.number().allow(null),
  dimmable: Joi.boolean(),
  dimmerType: Joi.string().allow('', null),
  ipRating: Joi.string().allow('', null),
  energyRating: Joi.string().allow('', null),
  heightCm: Joi.number().allow(null),
  widthCm: Joi.number().allow(null),
  depthCm: Joi.number().allow(null),
  diameterCm: Joi.number().allow(null),
  canopyDiameterCm: Joi.number().allow(null),
  minDropCm: Joi.number().allow(null),
  maxDropCm: Joi.number().allow(null),
  chainLengthCm: Joi.number().allow(null),
  weightKg: Joi.number().allow(null),
  installationType: Joi.string().valid(...INSTALLATION_TYPES),
  assemblyRequired: Joi.boolean(),
  certified: Joi.array().items(Joi.string()),
  warrantyMonths: Joi.number().integer(),
  careInstructions: Joi.string().allow(''),
  installationNotes: Joi.string().allow(''),
  customizationAvailable: Joi.boolean(),
  customizationNotes: Joi.string().allow(''),
  images: Joi.array().items(Joi.object().unknown(true)),
  lifestyleImages: Joi.array().items(Joi.object().unknown(true)),
  videos: Joi.array().items(Joi.object().unknown(true)),
  brochurePdf: Joi.object().unknown(true).allow(null),
  sizeGuideImage: Joi.object().unknown(true).allow(null),
  isFeatured: Joi.boolean(),
  isBestSeller: Joi.boolean(),
  isNewArrival: Joi.boolean(),
  isCustomOrder: Joi.boolean(),
  leadTimeDays: Joi.number().integer().min(0),
  sortOrder: Joi.number().integer(),
  relatedProductIds: Joi.array().items(Joi.string().hex().length(24)),
  seoTitle: Joi.string().allow(''),
  seoDescription: Joi.string().allow(''),
  seoKeywords: Joi.array().items(Joi.string()),
}).min(1);
